package it.uniroma3.siw.model;

import javax.persistence.*;


@Entity
public class MyOauthUser {

	@Id
	private Long id;
	
	@OneToOne(cascade = CascadeType.ALL)
	private User user;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}
	
	
	
}
